fun main(args: Array<String>) {
    var bikes = 5
    bikes + 1
    println(bikes) // 5

    bikes += 3
    println(bikes) // 8

    bikes -= 4
    println(bikes) // 4

    bikes *= 2
    println(bikes) // 8

    bikes /= 2
    println(bikes) // 4

    bikes = 5
    bikes %= 2
    println(bikes) // 1
}